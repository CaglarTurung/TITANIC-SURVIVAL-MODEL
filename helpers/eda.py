def cat_summary(dataframe, col_name, ratio=True, plot=False):
    if ratio:
        print(pd.DataFrame({col_name: dataframe[col_name].value_counts(),
                            "Ratio": dataframe[col_name].value_counts() / len(dataframe)}))
        print("##########################################")

    else:
        print(pd.DataFrame({col_name: dataframe[col_name].value_counts()}))
        print("##########################################")
    if plot:
        sns.countplot(x=dataframe[col_name], data=dataframe)
        plt.show()
def rare_analyser(dataframe, target, cat_cols):
    for col in cat_cols:
        print(col, ":", len(dataframe[col].value_counts()))
        print(pd.DataFrame({"COUNT": dataframe[col].value_counts(),
                            "RATIO": dataframe[col].value_counts() / len(dataframe),
                            "TARGET_MEAN": dataframe.groupby(col)[target].mean()}), end="\n\n\n")

def target_summary_with_num(dataframe, target, numerical_col):
    print(dataframe.groupby(target).agg({numerical_col: "mean"}), end="\n\n\n")
def target_summary_with_cat(dataframe, target, categorical_col):

    print(pd.DataFrame({"TARGET_MEAN": dataframe.groupby(categorical_col)[target].mean(), 'COUNT': dataframe.groupby(categorical_col)[target].count()}), end="\n\n\n")

def target_correlation_matrix(dataframe, target, corr_th=0.50, plot=False):
    corr = dataframe.corr()
    corr_th = corr_th
    try:
        filter = np.abs(corr[target]) > corr_th
        corr_features = corr.columns[filter].tolist()
        if plot:
            sns.clustermap(dataframe[corr_features].corr(), annot=True, fmt=".2f")
            plt.show()
        return corr_features
    except:
        print(f"There is no column that have more than {corr_th} correlation value with target {target} column. You may decrease threshold value")